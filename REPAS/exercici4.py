# Demanar a l’usuari dos valors. Una vegada s’executa el programa, aquest ha de mostrar el resultat per pantalla.
x = int(input('Introdueix valor 1: '))
y = int(input('Introdueix valor 2: '))

print(f'{x} + {y} = {x+y}')
print(f'{x} - {y} = {x-y}')
print(f'{x} * {y} = {x*y}')
print(f'{x} / {y} = {x/y}')